//
// Invert font data
//
void InvertBytes(byte *pData, byte bLen)
{
byte i;
   for (i=0; i<bLen; i++)
   {
      *pData = ~(*pData);
      pData++;
   }
} /* InvertBytes() */
//
// Delays of up to 60000 us
// This is not accurate, but is good enough
// to initialize the display controller
// which allows enough time for the high/low signals to get latched
//
void delay_us(uint16_t us)
{
  while (us)
  {
    __asm__ volatile (
        "nop" "\n\t"
        "nop" "\n\t"
        "nop" "\n\t"
        "nop"
    );
    us--;
  }
} /* delay_us() */
//
// Draw a string of small (6x8), normal (8x8) or large (16x16 or 16x32) characters
// At the given col+row
int hx1230WriteString(uint8_t x, uint8_t y, char *szMsg, uint8_t iSize, uint8_t bInvert)
{
int i;
unsigned char c, ucTemp[64];
const unsigned char *s;

  if (iSize == FONT_LARGE) // draw 16x32 font (or stretched 8x8 if on AVR)
  {
    i = 0;
    while (x < 96-15 && szMsg[i] != 0)
    {
// stretch the 'normal' font instead of using the big font
// to save FLASH space on AVR
#ifdef __AVR__
          int tx, ty;
          c = szMsg[i] - 32;
          unsigned char uc1, uc2, ucMask, *pDest;
          s = (unsigned char *)&ucFont[(int)c*8];
          memcpy_P(ucTemp, s, 8);
          if (bInvert)
              InvertBytes(ucTemp, 8);
          // Stretch the font to double width + double height
          memset(&ucTemp[8], 0, 32); // write 32 new bytes
          for (tx=0; tx<8; tx++)
          {
              ucMask = 3;
              pDest = &ucTemp[8+tx*2];
              uc1 = uc2 = 0;
              c = ucTemp[tx];
              for (ty=0; ty<4; ty++)
              {
                  if (c & (1 << ty)) // a bit is set
                      uc1 |= ucMask;
                  if (c & (1 << (ty + 4)))
                      uc2 |= ucMask;
                  ucMask <<= 2;
              }
              pDest[0] = uc1;
              pDest[1] = uc1; // double width
              pDest[16] = uc2;
              pDest[17] = uc2;
          }
          hx1230SetPosition(x, y);
          hx1230WriteDataBlock(&ucTemp[8], 16);
          hx1230SetPosition(x, y+1);
          hx1230WriteDataBlock(&ucTemp[24], 16);
#else
      s = &ucBigFont[(unsigned char)(szMsg[i]-32)*64];
      memcpy_P(ucTemp, s, 64);
      if (bInvert)
         InvertBytes(ucTemp, 64);
      hx1230SetPosition(x, y);
      hx1230WriteDataBlock(ucTemp, 16);
      hx1230SetPosition(x, y+1);
      hx1230WriteDataBlock(&ucTemp[16], 16);  
      hx1230SetPosition(x, y+2);
      hx1230WriteDataBlock(&ucTemp[32], 16);  
      hx1230SetPosition(x, y+3);
      hx1230WriteDataBlock(&ucTemp[48], 16);  
#endif // __AVR__
      i++;
      x += 16;
    } // while
  }
  if (iSize == FONT_NORMAL)// draw 8x8 font
   {
    hx1230SetPosition(x, y);
    i = 0;
    while (x < 96-7 && szMsg[i] !=0)
    {
      s = &ucFont[(unsigned char)(szMsg[i]-32) * 8];
      memcpy_P(ucTemp, s, 8); // copy from FLASH memory
      if (bInvert)
         InvertBytes(ucTemp, 8);
      hx1230WriteDataBlock(ucTemp, 8); // write character pattern
      i++;
      x += 8;
    } // while
   }
   else if (iSize == FONT_SMALL) // 6x8
   {
    hx1230SetPosition(x, y);
    i = 0;
    while (x < 96-5 && szMsg[i] !=0)
    {
      s = &ucSmallFont[(unsigned char)(szMsg[i]-32) * 6];
      memcpy_P(ucTemp, s, 6); // copy from FLASH memory
      if (bInvert)
         InvertBytes(ucTemp, 6);
      hx1230WriteDataBlock(ucTemp, 6); // write character pattern
      i++;
      x += 6;
    } // while
   }
  return 0;
} /* hx1230WriteString() */

// Fill the frame buffer with a byte pattern
// e.g. all off (0x00) or all on (0xff)
void hx1230Fill(byte ucData)
{
int x, y;
byte temp[32];

  memset(temp, ucData, 32);
  for (y=0; y<9; y++)
  {
    hx1230SetPosition(0,y); // set to (0,Y)
    for (x=0; x<3; x++)
    {
      hx1230WriteDataBlock(temp, 32); // fill with data byte
    } // for x
  } // for y;
} /* hx1230Fill() */

